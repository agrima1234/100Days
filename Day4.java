// Check whether the number is positive or negative
import java.util.*;
public class Day4 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        if(a>0)
            System.out.println(a + " is Positive.");
        else
            System.out.println(a + " is Negative");
    }

}
